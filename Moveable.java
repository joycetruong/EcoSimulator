interface Moveable {
  
  /**
   * up method
   * This method allows all animals to move upwards
   */
  public void up();
  
  /**
   * down method
   * This method allows all animals to move downwards
   */
  public void down();
  
  /**
   * left method
   * This method allows all animals to move to the left
   */
  public void left();
  
  /**
   * right method
   * This method allows all animals to move to the right
   */
  public void right();
  
  //public moved
  
}